
case "$1" in
    a)
        wtype -P 0x00E4 
        ;;
    A)
        wtype -P 0x00c4
        ;;
    o)
        wtype -P 0x00F6
        ;;
    O)
        wtype -P 0x00d6
        ;;
    u)
        wtype -P 0x00Fc
        ;;
    U)
        wtype -P 0x00dc
        ;;
    s)
        wtype -P 0x00DF
        ;;
    *)
        ;;
esac
